; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

; Name: Daniel Park | Date: 4/25/19 
; Purpose: The point of this code is to take the Ascii characters stored in x5000, x5001, and
; x5002 and print the character stored in x5002 with the characters stored in x5000 and x5001.
; I made the code in which 2 registers are used to create the x and y axis of my screen.
; I then multiplied the value of the data in x5002 by 16 in order to get the correct location
; of the FONT_DATA so I could use the data given to draw what I needed to draw. My loops then
; print the character stored in x5000 if the number is zero, and the character in x5001 if the 
; binary digit is 1. I left shift because that would allow me to check if the bit is 1 or 0.
; Then, if my column has reached 8, I used the new ascii line to create a new line to continue
; printing. Once my rows reached the end and reached 16, my program ends with the correct image 
; printed to the screen.

;R3 is Row Counter
;R4 is Column Counter
;R2 is the Offset for which Fill Data to start at
;R5 is the indicator of which line of font data we are at
;R6 is the memory inside the Font Data Address line
;R0 is the Character

	.ORIG   x3000 		; starting point of the code

	AND  	R3, R3, #0 	; Clear R3
   	ADD 	R3, R3, #8
	ADD 	R3, R3, #8	; Load 16 into R3
	
	AND 	R4, R4, #0	; Clear R4
	ADD 	R4, R4, #8	; Load 8 into R4

	LDI  	R2, CHAR	; Load M[x5002] into R2
	ADD 	R2, R2, R2	; Double R2
	ADD 	R2, R2, R2	; Double R2
	ADD 	R2, R2, R2	; Double R2
	ADD 	R2, R2, R2	; Double R2. Now original M[x5002] should be multiplied by
				; 16 to find what memory spot to go to in FONT_DATA

	AND	R5, R5, #0	; Clear R5
	LD  	R5, START	; Load the address of the first memory location of FONT_DATA
	ADD 	R5, R5, R2	; Add the R2 value to go to the memory line that contains
				; the correct pixel art for the character that we want.
	
LOAD	LDR 	R6, R5, #0	; Load the memory of the right FONT_DATA into R6
	AND 	R4, R4, #0	; Clear R4
	ADD 	R4, R4, #8	; Load 8 into R4

LOOP	ADD 	R6, R6, #0	; See the sign of R6
	BRn	NEG		; if R6 is neg go to the neg loop

POS	LDI  	R0, ZERO	; Load the memory of x5000 into R0
	OUT			; Print M[x5000]
	ADD	R6, R6, R6	; Double R6 to check the next bit
	ADD 	R4, R4, #-1	; Decrement R4
	BRp 	LOOP		; If it hasn't gone through 8 columns, loop
NEW	LD 	R0, NL		; Load New Line into R0
	OUT			; Print a new line
	ADD 	R5, R5, #1	; Increment R5
	ADD 	R3, R3, #-1	; decrement R3
	BRnz	STOP		; Once R3 is zero, stop the program
	BRnzp	LOAD		; Always go back to load

NEG	LDI 	R0, ONE
	OUT			; Print M[x5001]
	ADD 	R6, R6, R6	; Left shift R6 to check the next bit
	ADD 	R4, R4, #-1	; Decrement R4 to keep going down the columns
	BRp 	LOOP		; Loop if it has not gotten to column 8 or past
	BRnzp	NEW		; Always start a new line if passed 8 columns

STOP	HALT


NL	.FILL 	xA		; Variable to create a new line
ZERO	.FILL   x5000		; Variable to store address of the x5000 position
ONE	.FILL	x5001		; Variable to store the address if the bit is 1
CHAR	.FILL 	x5002		; Variable to store the address of the character we want to draw
START   .FILL   x3029		; Variable to store the address of the 
