;R0 is the register that holds the offset for FONT_DATA
;R1 is the row counter to offset the row each loop
;R2 is the register that counts the columns
;R3 is the register that holds the address of the letter to print
;R4 is the register for the rows during the loop
;R5 is the register that holds the actual address of where in FONT_DATA to go
;R6 is the register that holds the memory of FONT_DATA

	.ORIG	x3000

	AND 	R3, R3, #0	; Clear R3
	LD	R3, ONE		; Load x5001 into R3
	
	AND	R1, R1, #0	; Clear R1

	AND	R4, R4, #0	; Clear R4
	ADD	R4, R4, #8
	ADD	R4, R4, #8	; Set 16 into R4
	
LOADMEM	
	ADD	R3, R3, #1	; Add R3 + 1 into R3
	AND 	R0, R0, #0	; Clear R0
	LDR	R0, R3, #0	; Put M[R3] into R0. ex m[x5002] into R0
	ADD 	R0, R0, #0	; Add R0 with zero to see if the memory of the current address
				; is zero
	BRz	NEW		; Jump to new line

	ADD	R0, R0, R0	; Double R0
	ADD	R0, R0, R0	; Double R0
	ADD	R0, R0, R0	; Double R0
	ADD	R0, R0, R0	; Double R0. Now original M[R3] should be multiplied by 16
				; to find memory address of FONT_DATA

	AND 	R5, R5, #0	; Clear R5
	LD	R5, START	; Load in the first memory of FONT_DATA
	ADD	R5, R5, R1	; Add R1 to offset the row to the correct row of FONT_DATA
	ADD	R5, R5, R0	; Go to the lines with the correct pixel art in FONT_DATA

LOADDATA	
	LDR	R6, R5, #0	; Load the memory of the right FONT_DATA into R6
	AND 	R2, R2, #0	; Clear R2
	ADD	R2, R2, #8	; ADD 8 to R2

LOOP
	ADD	R6, R6, #0	; Get sign of R6
	BRn	NEG		; If R6 is neg, go to the neg loop

POS	
	AND	R0, R0, #0	; Clear R0
	LDI	R0, ZERO	; Load the memory of x5000 into R0
	OUT			; Print M[x5000]
	ADD 	R6, R6, R6	; Double R6 to left shift
	ADD	R5, R5, #1	; Increment R5
	ADD	R2, R2, #-1	; Decrement R2
	BRp	LOOP		; Loop if it hasn't gone through 8 columns
	BRnzp	LOADMEM		; Go to load mem after it prints 8 columns
NEW
	LD	R0, NL		; Load new line into R0
	OUT			; Print new line
	ADD	R4, R4, #-1	; Decrement column counter
	BRnz	STOP		; If rows gets past 16, go to next mem spot and get new letter 
				; to print
	AND 	R3, R3, #0	; Clear R3
	LD	R3, ONE		; Load x5001 into R3
	ADD	R1, R1, #1	; Increment R1 each time a row ends
	BRnzp	LOADMEM		; Always go back to LOADDATA if still in 16 rows
NEG
	AND 	R0, R0, #0	; Clear R0
	LDI	R0, ONE		; Load the memory of x5001 into R0
	OUT			; Print m[x5001]
	ADD	R6, R6, R6	; Double R6 to left shift
	ADD	R5, R5, #1	; Increment R5
	ADD 	R2, R2, #-1	; Decrement R2
	BRp	LOOP		; If not at 8th column yet, loop 
	BRnzp	LOADMEM		; Start a new line if passed 8 columns
STOP	
	HALT
	



